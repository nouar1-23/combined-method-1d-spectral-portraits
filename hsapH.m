function [] = hsapH(a, b, n)
% Вычисление спектрального портрета .
% 
% Алгоритм использует проектор для разделения матрицы на блоки,
% соответствующие собственным значениям внутри и вне окружности радиуса r,
% и решает уравнение Ляпунова итерационным методом.
%
% Входные параметры:
%   a - Начальный радиус сканирования
%   b - Конечный радиус сканирования
%   n - Размерность случайной матрицы

    % --- Начальные установки ---
    r1_val = a;           % Текущее значение параметра λ (радиус)
    i      = 1;           % Счетчик итераций для массивов
    l      = 0;           % Флаг пересчета проектора
    h      = 0.01;        % Шаг по параметру
    w_max  = 1e10;       % Ограничение нормы решения
    u_max  = 1e10;       % Ограничение обусловленности
    ip     = 1e-13;      % Точность вычислений
    
    A      = rand(n, n);  % Генерируем случайную матрицу
    I      = eye(n);      % Единичная матрица

    % Теоретическая оценка максимального числа итераций
    m0 = round(log2(- (1 + w_max) * log(ip / ((2 + 2 * ip) * sqrt(w_max)))));

    % --- Основной цикл сканирования по r ---
    while (r1_val < b)
        skip_step = false; 
        
        % Вычисление проектора выполняется только если l == 0
        if (l == 0)
            A0 = A / r1_val;
            B0 = I;
            p  = 2 * I;   % Начальное приближение проектора
            d  = 0;       % Счетчик внутренних итераций
            l  = 1;       % Устанавливаем флаг, что проектор в работе
            
            % Внутренний цикл вычисления проектора
            while max(abs(p^2 - p), [], 'all') > ip * max(abs(p), [], 'all') && d <= m0/3 
                d = d + 1;
                for i1 = 1:3
                    S_temp = [-B0; A0];
                    [Q_mat, ~] = qr(S_temp);
                    QQ     = Q_mat';
                    Q1_sub = QQ(n+1:2*n, 1:n);
                    Q2_sub = QQ(n+1:2*n, n+1:2*n);
                    A0     = Q1_sub * A0;
                    B0     = Q2_sub * B0;
                end

                if cond(A0 - B0) > u_max
                    skip_step = true;
                    break; 
                end
                p = -inv(A0 - B0) * B0; 
            end
            
            % Если проектор не сошелся
            if skip_step || isempty(p) || any(isnan(p(:)))
                e1(i) = r1_val;
                  m1(i)=NaN;
                l = 0; % Сброс флага для следующей попытки
                i = i + 1;
                r1_val = r1_val + h;
                fprintf('Предупреждение: проектор не сошелся при r = %f\n', r1_val);
                continue 
            end

            % --- Разделение пространства (SVD разложение проектора) ---
            t = round(trace(p)); % Размерность подпространства
            
            [U1, ~, ~] = svd(p);
            u1 = U1(:, 1:t);     % Базис подпространства внутри круга

            [U2, ~, ~] = svd(I - p);
            u2 = U2(:, 1:n-t);   % Базис подпространства вне круга

            % Матрица перехода к блочно-диагональному виду
            T = [u1, u2];
            in_T = inv(T);

            V = in_T * A * T;
            AA = V(1:t, 1:t);           % Блок "внутри"
            BB = V(t+1:n, t+1:n);       % Блок "снаружи"

            % Метрические матрицы
            W = T' * T;
            w11 = W(1:t, 1:t);
            w22 = W(t+1:n, t+1:n);

            inv_BB = inv(BB);
        end

        % --- Итерационное вычисление блоков решения ---
        
        % 1. Расчет для блока AA (внутри круга)
        aa = AA / r1_val;
        x0 = w11;
        x1 = x0 + (aa') * x0 * aa;
        s  = 0;
        while (t > 0 && max(abs(x1 - x0), [], 'all') > ip && max(abs(x1), [], 'all') < w_max && s < 10)
            s = s + 1;
            for j = 1:3
                aa = aa * aa;
                x0 = x1;
                x1 = x0 + aa' * x0 * aa;
            end
        end

        % 2. Расчет для блока BB (снаружи круга)
        bb = r1_val * inv_BB;
        y0 = bb' * w22 * bb;
        y1 = y0 + bb' * y0 * bb;
        s  = 0;
        while (t < n && max(abs(y1 - y0), [], 'all') > ip && max(abs(y1), [], 'all') < w_max && s < 10)
            s = s + 1;
            for j = 1:3
                bb = bb * bb;
                y0 = y1;
                y1 = y0 + bb' * y0 * bb;
            end
        end

        % --- Сборка общего решения и расчет нормы ---
        mm1 = zeros(t, n-t);
        zz  = [x1, mm1; mm1', y1];
        H1  = in_T' * zz * in_T;
      
        m1(i) = max(abs(H1), [], 'all');
        
        % Проверка на взрыв нормы
        if (m1(i) >= w_max)
            m1(i) = NaN;
            l = 0; % Требуется пересчет проектора
        end

        e1(i) = r1_val;
        i = i + 1;
        r1_val = r1_val + h;
    end

    % --- Визуализация ---
    figure;
    plot(e1, log10(m1), 'blue', 'LineWidth', 3);
    xlabel('|\lambda|', 'FontSize', 14, 'FontWeight', 'bold');
    ylabel('log_{10}||H||', 'FontSize', 14, 'FontWeight', 'bold');
    set(gca, 'FontSize', 25);
end